#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned char	i;

	i = 0;
	while (s[i] != '\0')
	{
		f(i, s[i]);
		i++;
	}
	return ((char *)s);
}
