#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*item;
	t_list	*head;

	while (lst)
	{
		item = ft_lstnew(f(lst->content));
		if (!item)
		{
			ft_lstclear(item, del);
			return (0);
		}
		ft_lstadd_back(&head, item);
		lst = lst->next;
	}
	return (head);
}
